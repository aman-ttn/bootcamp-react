
  function areaOfCricle(radius){
    return 3.14*radius*radius;
}

  function areaOfRectangle(width, height){
    return width*height;
}

  function areaOfCylinder(radius , height){
    var area=(2*3.14*radius*height)+(2*3.14*radius*radius);
    return area;
}
export{areaOfCricle,areaOfRectangle,areaOfCylinder};
